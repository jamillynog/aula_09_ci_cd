def area_of_rectangle(width, height):
    area = width*height
    return area
 
def perimeter_of_rectangle(width, height):
    perimeter = 2 * (width + height)
    return perimeter

def addition(n1,n2):
    soma = n1 + n2
    return soma

def subtraction(n1,n2):
    sub = n1 - n2
    return sub

def multiplication(n1,n2):
    mult = n1 * n2
    return mult

def division(n1,n2):
    div = n1 / n2
    return div

